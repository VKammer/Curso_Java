package Curso_Java_VK.operadores;

public class OperadoresRelacionais {
	
	public static void main(String[] args) {
		System.out.println(2 > 1); // maior que
		System.out.println(1 < 1); // menor que
		System.out.println(1 <= 1); // menor ou igual
		System.out.println(1 >= 2); // maior ou igual
		System.out.println(4 == 2); // igual a
		System.out.println(1 != 2); // diferente de
		
	}

}
